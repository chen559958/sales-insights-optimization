def get_district(address):
    for d in districts:
        if d in str(address):
            return d
    return None

df_clean['行政區'] = df_clean['送貨地址'].apply(get_district)
df_clean = df_clean.dropna(subset=['行政區'])


df_clean['訂單日期'] = pd.to_datetime(df_clean['訂單日期'])
df_clean['年份'] = df_clean['訂單日期'].dt.year
years = sorted(df_clean['年份'].dropna().unique())
num_years = len(years)


if num_years == 0:
    print("❌ 無法繪製圖表：資料中沒有有效的年份。")
else:
    fig, axes = plt.subplots(1, num_years, figsize=(7 * num_years, 7))
    if num_years == 1:
        axes = [axes]


    vibrant_colors = ['#1f77b4', '#ff7f0e', '#2ca02c', '#d62728', '#9467bd', '#e377c2', '#17becf']

    for i, year in enumerate(years):
        df_year = df_clean[df_clean['年份'] == year]
        unique_customers = df_year.drop_duplicates(subset=['客戶編號'])
        plot_data = unique_customers['行政區'].value_counts().reindex(districts).fillna(0)

        wedges, texts, autotexts = axes[i].pie(
            plot_data,
            autopct='%1.1f%%',
            startangle=90,
            colors=vibrant_colors,
            pctdistance=0.75,
            shadow=False
        )
        plt.setp(autotexts, fontweight='bold', fontsize=12, color='white')
        axes[i].set_title(f'{int(year)} 年度\n(有效客戶數: {int(plot_data.sum())} 人)',
                          fontsize=18, fontweight='bold', pad=15)

    fig.legend(wedges, districts,
               title="行政區域",
               loc="lower center",
               bbox_to_anchor=(0.5, 0.05),
               ncol=7,
               fontsize=12,
               prop={'weight': 'bold'})

