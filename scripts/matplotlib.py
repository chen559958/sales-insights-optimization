    df['訂單日期'] = pd.to_datetime(df['訂單日期'])


    monthly_sales = df.groupby(df['訂單日期'].dt.to_period('M'))['小計'].sum().reset_index()
    monthly_sales = monthly_sales.sort_values('訂單日期')


    new_labels = []
    for dt in monthly_sales['訂單日期']:
        if dt.month == 1:
            new_labels.append(f"1\n{dt.year}")
        else:
            new_labels.append(str(dt.month))


    plt.figure(figsize=(15, 6))


    plt.plot(range(len(monthly_sales)), monthly_sales['小計'],
             color='#008B8B', marker='o', linewidth=3, markersize=6, alpha=1)

    plt.title('每月營業額趨勢', fontsize=22, fontweight='bold', pad=30)
    plt.ylabel('銷售額 (元)', fontsize=14, fontweight='bold')


    plt.xticks(range(len(monthly_sales)), new_labels, fontsize=11, fontweight='bold')
    plt.yticks(fontsize=12, fontweight='bold')


    plt.xlabel('  ', loc='right')
    plt.annotate('(月)', xy=(1, 0), xycoords='axes fraction',
                 xytext=(20, -15), textcoords='offset points',
                 ha='left', va='top', fontsize=12, fontweight='bold')


